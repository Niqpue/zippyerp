/**
 * финансовый отчет  малого  предприятия
 */
class FinancialReportSmall extends \ZippyERP\ERP\Pages\Base
{

    public function __construct()
    {
        parent::__construct();

        $this->add(new Form('filter'))->setSubmitHandler($this, 'OnSubmit');
        $this->filter->add(new DropDownChoice('yr'));
        $this->filter->add(new DropDownChoice('qw'));

        $this->add(new Panel('detail'))->setVisible(false);
        $this->detail->add(new RedirectLink('print', ""));
        $this->detail->add(new RedirectLink('html', ""));
        $this->detail->add(new RedirectLink('word', ""));
        $this->detail->add(new RedirectLink('excel', ""));
        $this->detail->add(new Label('preview'));
    }

    public function OnSubmit($sender)
    {

        $html = $this->generateReport();
        $this->detail->preview->setText($html, true);

        \ZippyERP\System\Session::getSession()->printform = "<html><head><meta http-equiv=\"Content-Type\" content=\"text/html; charset=UTF-8\"></head><body>" . $html . "</body></html>";
        $reportpage = "ZippyERP/ERP/Pages/ShowDoc";
        $this->detail->print->pagename = $reportpage;
        $this->detail->print->params = array('print', "finreport25");
        $this->detail->html->pagename = $reportpage;
        $this->detail->html->params = array('html', "finreport25");
        $this->detail->word->pagename = $reportpage;
        $this->detail->word->params = array('doc', "finreport25");
        $this->detail->excel->pagename = $reportpage;
        $this->detail->excel->params = array('xls', "finreport25");

        $this->detail->setVisible(true);
    }

    private function generateReport()
    {


        $detail = array();
        $totstartdt = 0;
        $totstartct = 0;
        $totobdt = 0;
        $totobct = 0;
        $totenddt = 0;
        $totendct = 0;

        $year = $this->filter->yr->getValue();
        $qw = $this->filter->qw->getValue();

        $date = new Carbon();
        $date->year($year)->startOfYear();
        $from = $date->timestamp;
        $date->addMonths($qw);
        $to = $date->timestamp - 1;

        $firm = System::getOptions("firmdetail");

        $a20 = Account::load(20);
        $a22 = Account::load(22);
        $a23 = Account::load(23);
        $a26 = Account::load(26);
        $a28 = Account::load(28);

        $b1100 = $a20->getSaldo($from) + $a22->getSaldo($from) + $a23->getSaldo($from);
        $e1100 = $a20->getSaldo($to) + $a22->getSaldo($to) + $a23->getSaldo($to);
        $b1103 = $a26->getSaldo($from) + $a28->getSaldo($from);
        $e1103 = $a26->getSaldo($to) + $a28->getSaldo($to);

        $b1100 = $b1100 + $b1103;
        $e1100 = $e1100 + $e1103;


        $header = array(
            'date1' => date('Y m d', time()),
            'date2' => date('d.m.Y', $to + 1),
            'edrpou' => (string) sprintf("%10d", $firm['edrpou']),
            'b1100' => H::fm_t1($b1100),
            'e1100' => H::fm_t1($e1100),
            'e1103' => H::fm_t1($b1103),
            'e1103' => H::fm_t1($e1103)
        );

        $report = new \ZippyERP\ERP\Report('financialreportsmall.tpl');

        $html = $report->generate($header, $detail);

        return $html;
    }

}

